#Exercicio 3 - Lista 1 : Verificar se duas strings tem o mesmo comprimento e conteudo

a = input ("Digite a primeira string\n")
b = input ("Digite a outra string\n")
if (len(a)==len(b)):
    print("As strings tem o mesmo comprimento!")
else:
    print("As strings nao tem o mesmo comprimento!")
if (a==b):
    print("Ainda mais, elas sao iguais.")
else:
    print("Ainda mais, elas sao diferentes.")

